/*                     changing number in string , boolean data types 
let marksinmath = 54
console.log(typeof marksinmath);

// changing to string 
let intostring = String (marksinmath);
console.log(intostring); //  54
console.log(typeof intostring);  //string

// changing to boolean 
let intoboolean = Boolean (marksinmath);
console.log(intoboolean); //true
console.log(typeof intoboolean);   // boolean
*/


